package dto;

public class DriverDTO {
	private String did;
	private String name;
	private String address;
	private String mobile;
	private String id_proof;
	private String lic_no;
	private String exp_lic_date;
	public String getDid() {
		return did;
	}
	public void setDid(String did) {
		this.did = did;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getId_proof() {
		return id_proof;
	}
	public void setId_proof(String id_proof) {
		this.id_proof = id_proof;
	}
	public String getLic_no() {
		return lic_no;
	}
	public void setLic_no(String lic_no) {
		this.lic_no = lic_no;
	}
	public String getExp_lic_date() {
		return exp_lic_date;
	}
	public void setExp_lic_date(String exp_lic_date) {
		this.exp_lic_date = exp_lic_date;
	}
	
	
}
